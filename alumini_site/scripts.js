const candidatesContainer = document.getElementById("candidatesContainer");
const candidateModal = document.getElementById("candidateModal");
const modalContent = document.getElementById("modalContent");

// Function to create candidate cards
function createCandidateCard(candidate) {
  const card = document.createElement("div");
  card.classList.add("candidate-card");
  card.innerHTML = `
        <img src="${candidate.profilePicture}" alt="${candidate.name}">
        <h3>${candidate.name}</h3>
        <h6>${candidate.about}</h6>
        
    `;
  card.addEventListener("click", () => openModal(candidate));
  return card;
}

// Function to display detailed candidate information in the modal
function openModal(candidate) {
  modalContent.innerHTML = `
        <span class="close" onclick="closeModal()">&times;</span>
        <img src="${candidate.profilePicture}" alt="${candidate.name}">
        <h2>${candidate.name}</h2>
        <p>Section: ${candidate.section}</p>
        <p>Resume: <a href="${candidate.resume}" target="_blank">View Resume</a></p>
        <p>LinkedIn: <a href="${candidate.linkedin}" target="_blank">${candidate.linkedin}</a></p>
        <p>GitHub: <a href="${candidate.github}" target="_blank">${candidate.github}</a></p>
        <p>Project: <a href="${candidate.project}" target="_blank">${candidate.project}</a></p>
    `;
  candidateModal.style.display = "block";
}

// Function to close the modal
function closeModal() {
  candidateModal.style.display = "none";
}

// Initialize the webpage with candidate cards
candidates.forEach((candidate) => {
  candidatesContainer.appendChild(createCandidateCard(candidate));
});
